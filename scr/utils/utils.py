def add(x: int, y: int) -> int:
    """
    `add` takes two integers, `x` and `y`, and returns their sum
    - Args:
      x (int): int
      y (int): int
    - Returns:
      The sum of x and y
    """
    return x+y


def sub(x: int, y: int) -> int:
    """
    `sub` takes two integers, `x` and `y`, and returns their difference
    - Args:
      x (int): int
      y (int): int
    - Returns:
      The difference between x and y
    """
    return x-y


def mul(x: int, y: int) -> int:
    """
    `mul` takes two integers, `x` and `y`, and returns their product
    - Args:
      x (int): int
      y (int): int
    - Returns:
      the product of x and y.
    """
    return x*y


def div(x: int, y: int) -> float:
    """
     `div` takes two integers as arguments and returns a float.
    - Args:
      x (int): int
      y (int): int
    - Returns:
      the value of x/y.
    """
    return x/y
